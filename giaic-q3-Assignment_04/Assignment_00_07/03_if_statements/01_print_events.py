def main():
    # Loop will iterate 20 times to print 20 even numbers
    for i in range(20):  
        # Print the even number (i * 2), and use 'end=" "' to print them on the same line
        print(i * 2, end=" ")  

# Check if the script is being run directly
if __name__ == '__main__':
    main()  # Call the main function to execute the program
